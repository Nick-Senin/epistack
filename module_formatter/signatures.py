"""
Сигнатуры для модуля форматирования текста
"""
import dspy


class FormatterSignature(dspy.Signature):
    """
    Отформатируй текст, полностью сохраняя его содержимое.
    
    Твоя задача:
    1. Улучшить читаемость через правильные абзацы и переносы строк
    2. Применить markdown форматирование:
    3. Исправить пробелы и пунктуацию (лишние пробелы, отсутствующие знаки)
    
    КРИТИЧЕСКИ ВАЖНО:
    - НЕ добавляй новую информацию
    - НЕ удаляй существующую информацию
    - НЕ изменяй смысл текста
    - Сохрани все факты, цифры, имена и детали
    - Только улучши визуальное представление
    
    Формат ответа: чистый markdown текст без дополнительных комментариев.
    """
    
    text = dspy.InputField(desc="Исходный неотформатированный текст")
    
    formatted_text = dspy.OutputField(desc="Отформатированный текст в markdown с сохранением всего содержимого")
